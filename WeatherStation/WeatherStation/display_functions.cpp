void writeRainGaugeDisplay(float rainTotal, int tipCount) {
  tcaselect(2);
  display.clearDisplay();
  display.setTextColor(SSD1309_PIXEL_ON);

  // Draw bigger cloud, slightly lower and farther right
  display.drawBitmap(CLOUD_X, CLOUD_Y, cloud_bmp, CLOUD_W, CLOUD_H, SSD1309_PIXEL_ON);

  // Draw and update rain
  for (int i = 0; i < NUM_RAINDROPS; i++) {
    // Shorter raindrops
    display.drawLine(rainX[i], rainY[i], rainX[i], rainY[i] + 1, SSD1309_PIXEL_ON);

    // Move downward
    rainY[i] += rainSpeed[i];

    // When a drop leaves the screen, respawn it UNDER the cloud
    if (rainY[i] > SCREEN_HEIGHT) {
      rainY[i] = random(RAIN_START_Y, RAIN_START_Y + 18);
      rainX[i] = random(RAIN_MIN_X, RAIN_MAX_X + 1);
    }
  }

  // Text
  display.setTextSize(1);

  display.setCursor(0, 20);
  display.println("Rain Gauge");

  display.setCursor(0, 34);
  display.print("Rain: ");
  display.print(rainTotal, 2);
  display.println(" in");

  display.display();
}

void writeServoDisplay(int servoPosition, ServoState servoState) {
  tcaselect(2);
  display.clearDisplay();
  display.setTextColor(SSD1309_PIXEL_ON);
  display.setTextSize(1);

  // Title
  display.setCursor(0, 0);
  display.println("Solar Panel Position");

  // Animation area:
  // Keep icon below title, moving horizontally across the screen
  int iconY = 12;
  int iconX = map(servoPosition, 0, 180, 0, 112);  // 128 - 16 = 112 max for 16px icon
  int frame = 0;

  // Choose sun or moon depending on state
  if (servoState == 0) {   // SERVO_SWEEPING
    display.drawBitmap(iconX, iconY, frames[frame], FRAME_WIDTH, FRAME_HEIGHT, SSD1309_PIXEL_ON);
  }
  else if (servoState == 1) {   // SERVO_RETURNING
    display.drawBitmap(iconX, iconY, moon_bmp, FRAME_WIDTH, FRAME_HEIGHT, SSD1309_PIXEL_ON);
  }
  else if (servoState == 2) {   // SERVO_WAITING
    // Show moon parked on the left during waiting/night
    display.drawBitmap(0, iconY, moon_bmp, FRAME_WIDTH, FRAME_HEIGHT, SSD1309_PIXEL_ON);
  }

  // Optional horizon line for visual grounding
  display.drawLine(0, 46, 127, 46, SSD1309_PIXEL_ON);

  // Status text only (angle removed)
  display.setCursor(0, 50);
  display.print("Mode: ");

  if (servoState == 0) {
    display.println("Day");
  }
  else if (servoState == 1) {
    display.println("Reset");
  }
  else if (servoState == 2) {
    display.println("Night");
  }

  display.display();
}

